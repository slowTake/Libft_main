#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (s1[n] || s2[n])
	{
		if (s1[n] == s2[n])
		{
			n++;
		}
		else
			return (s1[n] - s2[n]);
	}
	return (0);
}